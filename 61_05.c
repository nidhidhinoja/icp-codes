//5.    Input two numbers and prints its square and cube.//
#include<stdio.h>
int main()
{
	int k,i;
	printf("enter the value of k:");
	scanf("\n%d",&k);
	 
	printf("enter the value of i:");
	scanf("\n%d",&i);
	
	printf("\nsqure %d",k*k);
	printf("\nsqure %d",i*i);
	printf("\ncube %d",k*k*k);
	printf("\ncube %d",i*i*i);
	return 0;
}
